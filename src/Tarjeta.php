<?php

namespace TpFinal;

class Tarjeta {
    Protected $saldotarjeta;
    public function saldo($a) {
        if ($a=332) {
                $saldotarjeta = $saldotarjeta + 388;
            }
        else {
                $saldotarjeta = $saldotarjeta + $a;
            }
        return 0;
    }
    public function viaje {
        $saldotarjeta = $saldotarjeta - 9.75;
    }
}
