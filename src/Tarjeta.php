<?php

namespace TpFinal;

class Tarjeta {
    Protected $saldotarjeta;
    public function saldo($a) {
        if ($a=332) {
                $this->saldotarjeta = $this->saldotarjeta + 388;
            }
        else {
                $this->$saldotarjeta = $this->$saldotarjeta + $a;
            }
        return 0;
    }
    public function viaje {
        $this->saldotarjeta = $this->saldotarjeta - 9.75;
    }
}
